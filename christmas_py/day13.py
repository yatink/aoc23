def check_vertical_mismatches(pattern: list[str], mirror_column_index: int) -> bool:
    lines = pattern.split("\n")
    num_cols = len(lines[0])
    num_rows = len(lines)
    cols_to_compare = list(zip(range(mirror_column_index,-1 ,-1), range(mirror_column_index + 1, num_cols)))
    if len (cols_to_compare) == 0:
        return 1000000
    mismatches = 0
    for row in range(num_rows):
        for (colA, colB) in cols_to_compare:
            if lines[row][colA] != lines[row][colB]:
                mismatches += 1
    return mismatches


def check_horizontal_mismatches(pattern: list[str], mirror_row_index: int) -> bool:
    lines = pattern.split("\n")
    num_cols = len(lines[0])
    num_rows = len(lines)
    rows_to_compare = list(zip(range(mirror_row_index,-1 ,-1), range(mirror_row_index + 1, num_rows)))
    if len(rows_to_compare) == 0:
        return 1000000
    mismatches = 0
    for col in range(num_cols):
        for rowA, rowB in rows_to_compare:
            if lines[rowA][col] != lines[rowB][col]:
                mismatches += 1
    return mismatches


